package nv.module.brushdraw.ui.customview;

public class PointXY {
    private float x,y;

    public PointXY(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public PointXY() {
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
